public class _07_AccessModifiers {
  public static void main(String[] args) {
    /*
    Access Modifiers / Visibility Modifiers
      public  
      default --> Package Private
      protected --> Inheritance Private
      private --> Class Private
    */
    System.out.println("Access Modifiers OR Visibility Modifiers:");
    System.out.println("private: \taccessible only within the class");
    System.out.println("default: \taccessible only within the package");
    System.out.println("protected: \taccessible within the package and sub-class");
    System.out.println("public: \taccessible everywhere");
  }
}
